          <p class="single-ingredient">4 eggs, soft-boiled</p>
          <p class="single-ingredient">4 green onions, sliced</p>
          <p class="single-ingredient">4 sheets of nori seaweed</p>
          <p class="single-ingredient">1 cup bean sprouts</p>
          <p class="single-ingredient">1 cup sliced shiitake mushrooms</p>
          <p class="single-ingredient">1/2 cup corn kernels</p>
          <p class="single-ingredient">1/4 cup chopped cilantro</p>
