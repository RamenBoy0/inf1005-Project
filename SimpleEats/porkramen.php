    <section class="recipe-guide">
        <h4>How to cook?</h4>
        <div class="steps">
            <h5>Step 1</h5>
          <p>
            Preheat the oven to 375Â°F. Place the pork bones in a roasting pan 
            and roast for 30-40 minutes or until browned.
          </p>
        </div>
        <div class="steps">
            <h5>Step 2</h5>
          </header>
          <p>
             In a large pot, combine the onion, ginger, garlic, water, chicken 
             broth, soy sauce, mirin, sake, brown sugar, and salt. Add the 
             roasted pork bones to the pot and bring the mixture to a boil.
          </p>
        </div>
        <div class="steps">
            <h5>Step 3</h5>
          <p>
            Reduce the heat to low and simmer the broth for 6-8 hours, skimming 
            off any impurities that rise to the top. Strain the broth and discard 
            the solids.
          </p>
        </div>
        <div class="steps">
            <h5>Step 4</h5>
          <p>
            In a large skillet, heat the vegetable oil over medium-high heat. 
            Add the sliced pork belly and cook until browned and crispy. Season 
            with salt and pepper to taste.
          </p>
        </div>
        <div class="steps">
            <h5>Step 5</h5>
          <p>
            In a separate pot, cook the ramen noodles according to the package 
            instructions. Drain the noodles and set them aside.
          </p>
        </div>
        <div class="steps">
            <h5>Step 6</h5>
          <p>
            To assemble the ramen bowls, divide the noodles among 4 bowls. 
            Ladle the hot broth over the noodles.
          </p>
        </div>
        <div class="steps">
            <h5>Step 7</h5>
          <p>
            Top each bowl with sliced pork belly, soft-boiled egg, green onions, 
            nori seaweed, bean sprouts, shiitake mushrooms, corn kernels, and 
            cilantro.
          </p>
        </div>
        <div class="steps">
            <h5>Step 8</h5>
          <p>
             Drizzle each bowl with toasted sesame oil and serve hot.
          </p>
        </div>
