          <p class="single-ingredient">2-inch piece ginger, sliced</p>
          <p class="single-ingredient">6 garlic cloves, crushed</p>
          <p class="single-ingredient">6 cups water</p>
          <p class="single-ingredient">2 cups chicken broth</p>
          <p class="single-ingredient">1/4 cup soy sauce</p>
          <p class="single-ingredient">1/4 cup mirin</p>
