          <h4>Ingredients</h4>
          <p class="single-ingredient">1 lb boneless pork loin, cut into bite-sized pieces</p>
          <p class="single-ingredient">1/2 cup cornstarch</p>
          <p class="single-ingredient">1/4 cup all-purpose flour</p>
          <p class="single-ingredient">1/2 teaspoon salt</p>
          <p class="single-ingredient">1/2 teaspoon black pepper</p>
          <p class="single-ingredient">1 egg, beaten</p>
          <p class="single-ingredient">1/2 cup vegetable oil</p>
          <p class="single-ingredient">1 red bell pepper, sliced</p>
          <p class="single-ingredient">1 green bell pepper, sliced</p>
          <p class="single-ingredient">1 onion, sliced</p>
          <p class="single-ingredient">1 can pineapple chunks, drained</p>
          <h5>Sauce:</h5>
          <p class="single-ingredient">1/4 cup ketchup</p>
          <p class="single-ingredient">1/4 cup rice vinegar</p>
          <p class="single-ingredient">1/4 cup brown sugar</p>
          <p class="single-ingredient">1 tablespoon soy sauce</p>
          <p class="single-ingredient">1 tablespoon cornstarch</p>
          <p class="single-ingredient">1/4 cup water</p>
    </section>
    
    <section class="recipe-guide">
        <h4>How to cook?</h4>
        <div class="steps">
            <h5>Step 1</h5>
          <p>
            In a bowl, combine the cornstarch, flour, salt, and black pepper. 
            In another bowl, beat the egg.
          </p>
        </div>
        <div class="steps">
            <h5>Step 2</h5>
          <p>
            Dip the pork pieces in the beaten egg, then coat them with the 
            cornstarch mixture.
          </p>
        </div>
        <div class="steps">
            <h5>Step 3</h5>
          <p>
             Heat the vegetable oil in a wok or large skillet over medium-high 
             heat. Add the pork pieces and stir-fry for 5-7 minutes until they 
             are golden brown and cooked through. Remove the pork from the wok 
             and set aside.
          </p>
        </div>
        <div class="steps">
            <h5>Step 4</h5>
          <p>
            In the same wok, add the sliced red and green bell peppers, onion, 
            and pineapple chunks. Stir-fry for 2-3 minutes until the vegetables 
            are slightly softened.
          </p>
        </div>
        <div class="steps">
            <h5>Step 5</h5>
          <p>
            In a small bowl, whisk together the ketchup, rice vinegar, brown 
            sugar, soy sauce, cornstarch, and water to make the sauce.
          </p>
        </div>
        <div class="steps">
            <h5>Step 6</h5>
          <p>
            Add the sauce to the wok and stir-fry for another 2-3 minutes until 
            the sauce thickens and coats the vegetables.
          </p>
        </div>
        <div class="steps">
            <h5>Step 7</h5>
          <p>
            Add the cooked pork to the wok and stir-fry for another minute until 
            the pork is coated in the sauce.
          </p>
        </div>
        <div class="steps">
            <h5>Step 8</h5>
          <p>
            Taste and adjust seasoning, adding salt or sugar if needed.
          </p>
        </div>
        <div class="steps">
            <h5>Step 9</h5>
          <p>
            Serve hot with steamed rice.
          </p>
        </div>
