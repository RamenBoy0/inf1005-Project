          <h4>Ingredients</h4>
          <p class="single-ingredient">1 pound spaghetti or other pasta</p>
          <p class="single-ingredient">6 slices of bacon, chopped</p>
          <p class="single-ingredient">3 cloves of garlic, minced</p>
          <p class="single-ingredient">3 large egg yolks</p>
          <p class="single-ingredient">1/2 cup grated Parmesan cheese</p>
          <p class="single-ingredient">Salt and black pepper, to taste</p>
          <p class="single-ingredient">Fresh parsley, chopped (optional)</p>
    </section>
      
    <section class="recipe-guide">
        <h4>How to cook?</h4>
        <div class="steps">
            <h5>Step 1</h5>
          <p>
            Cook the pasta in a large pot of salted boiling water according to 
            the package instructions until al dente. Reserve 1/2 cup of the pasta 
            cooking water, then drain the pasta and set aside.
          </p>
        </div>
        <div class="steps">
            <h5>Step 2</h5>
          <p>
             Meanwhile, cook the chopped bacon in a large skillet over medium 
             heat until crispy. Remove the bacon with a slotted spoon and set 
             aside on a plate lined with paper towels.
          </p>
        </div>
        <div class="steps">
            <h5>Step 3</h5>
          <p>
            Add the minced garlic to the skillet with the bacon grease and cook 
            for 1-2 minutes until fragrant.
          </p>
        </div>
        <div class="steps">
            <h5>Step 4</h5>
          <p>
            In a small bowl, whisk together the egg yolks, Parmesan cheese, and 
            a pinch of salt and black pepper.
          </p>
        </div>
        <div class="steps">
            <h5>Step 5</h5>
          <p>
            Reduce the heat to low and add the cooked pasta to the skillet with 
            the garlic. Toss to coat the pasta in the bacon grease.
          </p>
        </div>
        <div class="steps">
            <h5>Step 6</h5>
          <p>
            Remove the skillet from the heat and quickly stir in the egg mixture 
            until the pasta is coated in a creamy sauce. If the sauce is too thick, 
            add some of the reserved pasta cooking water to thin it out.
          </p>
        </div>
        <div class="steps">
            <h5>Step 7</h5>
          <p>
            Add the crispy bacon to the skillet and toss to combine. Taste and 
            adjust the seasoning with more salt and black pepper, if needed.
          </p>
        </div>
        <div class="steps">
            <h5>Step 8</h5>
          <p>
             Serve hot, garnished with chopped parsley, if desired.
          </p>
        </div>
