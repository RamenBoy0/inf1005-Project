          <p class="single-ingredient">1 lb boneless, skinless chicken breast, cut into bite-sized pieces</p>
          <p class="single-ingredient">2 tablespoons vegetable oil</p>
          <p class="single-ingredient">2 garlic cloves, minced</p>
          <p class="single-ingredient">1 tablespoon grated fresh ginger</p>
          <p class="single-ingredient">1/2 cup roasted peanuts</p>
          <p class="single-ingredient">1 red bell pepper, sliced</p>
          <p class="single-ingredient">1 green bell pepper, sliced</p>
          <p class="single-ingredient">2 green onions, sliced</p>
