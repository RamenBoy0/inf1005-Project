          <p class="single-ingredient">1/4 teaspoon salt</p>
          <h5>Marinade:</h5>
          <p class="single-ingredient">1 tablespoon soy sauce</p>
          <p class="single-ingredient">1 tablespoon rice wine vinegar</p>
          <p class="single-ingredient">1 tablespoon cornstarch</p>
          <p class="single-ingredient">1 tablespoon vegetable oil</p>
    </section>
      
    <section class="recipe-guide">
        <h4>How to cook?</h4>
        <div class="steps">
            <h5>Step 1</h5>
          <p>
            In a bowl, combine the chicken pieces with the marinade ingredients 
            and mix well. Let the chicken marinate for at least 30 minutes.
          </p>
        </div>
        <div class="steps">
            <h5>Step 2</h5>
          <p>
            In a small bowl, mix together the cornstarch, soy sauce, rice wine 
            vinegar, and brown sugar to make the sauce.
          </p>
        </div>
        <div class="steps">
            <h5>Step 3</h5>
          <p>
            Heat the vegetable oil in a wok or large skillet over high heat. 
            Add the garlic, ginger, and Sichuan peppercorns (if using), and 
            stir-fry for about 30 seconds until fragrant.
          </p>
        </div>
        <div class="steps">
            <h5>Step 4</h5>
          <p>
            Add the chicken to the wok and stir-fry for 4-5 minutes until it is 
            cooked through and no longer pink.
          </p>
        </div>
        <div class="steps">
            <h5>Step 5</h5>
          <p>
            Add the red and green bell peppers, and stir-fry for another 2-3 
            minutes until they are slightly softened.
          </p>
        </div>
        <div class="steps">
            <h5>Step 6</h5>
          <p>
            Add the peanuts, green onions, and the sauce mixture to the wok. 
            Stir-fry for another 2-3 minutes until the sauce thickens and coats the chicken and vegetables.
          </p>
        </div>
        <div class="steps">
            <h5>Step 7</h5>
          <p>
            Taste and adjust seasoning, adding salt if needed.
          </p>
        </div>
        <div class="steps">
            <h5>Step 8</h5>
          <p>
            Serve hot with steamed rice.
          </p>
        </div>
