    <section class="recipe-guide">
        <h4>How to cook?</h4>
        <div class="steps">
            <h5>Step 1</h5>
          <p>
            Heat the vegetable oil in a large skillet over medium-high heat. 
            Add the chopped onions and sauté until softened and translucent.
          </p>
        </div>
        <div class="steps">
            <h5>Step 2</h5>
          <p>
            Add the minced garlic and ginger paste and sauté for another minute.
          </p>
        </div>
        <div class="steps">
            <h5>Step 3</h5>
          <p>
            Add the chopped tomatoes and cook until they break down and turn 
            into a thick paste.
          </p>
        </div>
        <div class="steps">
            <h5>Step 4</h5>
          <p>
            Add the cumin powder, coriander powder, turmeric powder, red chili 
            powder, and salt, and stir until the spices are well combined.
          </p>
        </div>
        <div class="steps">
            <h5>Step 5</h5>
          <p>
            Add the chicken pieces and cook until they are browned on all sides.
          </p>
        </div>
        <div class="steps">
            <h5>Step 6</h5>
          <p>
            Pour in the water, cover the skillet, and simmer for 15-20 minutes 
            or until the chicken is cooked through and the sauce has thickened.
          </p>
        </div>
        <div class="steps">
            <h5>Step 7</h5>
          <p>
            Garnish with chopped cilantro leaves and serve hot with rice or naan.
          </p>
        </div>
