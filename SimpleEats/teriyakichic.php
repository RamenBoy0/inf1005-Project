    <section class="recipe-ingredient">
          <h4>Ingredients</h4>
          <h5>Chicken:</h5>
          <p class="single-ingredient">4 boneless, skinless chicken breasts</p>
          <p class="single-ingredient">1/2 cup soy sauce</p>
          <p class="single-ingredient">1/4 cup brown sugar</p>
          <p class="single-ingredient">2 tablespoons honey</p>
          <p class="single-ingredient">2 tablespoons rice vinegar</p>
          <p class="single-ingredient">1 tablespoon minced garlic</p>
          <p class="single-ingredient">1 tablespoon minced ginger</p>
          <p class="single-ingredient">1 tablespoon cornstarch</p>
          <p class="single-ingredient">1/4 cup water</p>
          <p class="single-ingredient">2 tablespoons vegetable oil</p>
          <p class="single-ingredient">Salt and pepper, to taste</p>
          <h5>Rice:</h5>
          <p class="single-ingredient">1 cup long-grain white rice</p>
          <p class="single-ingredient">2 cups water</p>
          <p class="single-ingredient">1/2 teaspoon salt</p>
          <p class="single-ingredient">1 tablespoon butter</p>
    </section>
   
    <section class="recipe-guide">
        <h4>How to cook?</h4>
        <div class="steps">
            <h5>Step 1</h5>
          <p>
            In a medium bowl, whisk together the soy sauce, brown sugar, honey, 
            rice vinegar, garlic, and ginger until well combined.
          </p>
        </div>
        <div class="steps">
            <h5>Step 2</h5>
          <p>
            Place the chicken breasts in a large ziplock bag and pour the 
            teriyaki sauce over them. Seal the bag and marinate the chicken in 
            the refrigerator for at least 30 minutes or up to 2 hours.
          </p>
        </div>
        <div class="steps">
            <h5>Step 3</h5>
          <p>
            In a small bowl, whisk together the cornstarch and water until 
            smooth to make a slurry.
          </p>
        </div>
        <div class="steps">
            <h5>Step 4</h5>
          <p>
            Heat the vegetable oil in a large skillet over medium-high heat. 
            Remove the chicken from the marinade, reserving the marinade, and 
            season the chicken with salt and pepper.
          </p>
        </div>
        <div class="steps">
            <h5>Step 5</h5>
          <p>
            Add the chicken to the skillet and cook for 5-6 minutes per side or 
            until golden brown and cooked through.
          </p>
        </div>
        <div class="steps">
            <h5>Step 6</h5>
          <p>
            Transfer the chicken to a plate and tent with foil to keep warm.
          </p>
        </div>
        <div class="steps">
            <h5>Step 7</h5>
          <p>
            In the same skillet, add the reserved teriyaki marinade and bring to 
            a simmer. Add the cornstarch slurry and stir until the sauce thickens, 
            about 2-3 minutes.
          </p>
        </div>
        <div class="steps">
            <h5>Step 8</h5>
          <p>
            For the rice, rinse the rice in a fine mesh strainer under cold 
            running water until the water runs clear. Add the rinsed rice, water, 
            salt, and butter to a medium saucepan.
          </p>
        </div>
        <div class="steps">
            <h5>Step 9</h5>
          <p>
            Bring the water to a boil, then reduce the heat to low and cover the 
            saucepan with a tight-fitting lid. Simmer for 18-20 minutes or until 
            the rice is tender and the water is absorbed.
          </p>
        </div>
        <div class="steps">
            <h5>Step 10</h5>
          <p>
            Serve the chicken over the rice and spoon the teriyaki sauce over 
            the top. Garnish with sliced green onions and sesame seeds, if desired.
          </p>
        </div>
    </section>   
